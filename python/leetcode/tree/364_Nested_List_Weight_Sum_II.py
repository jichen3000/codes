class Solution(object):
    def depthSumInverse(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        from collections import defaultdict
        cur_list = nestedList
        mem = defaultdict(list)
        level = 0
        while cur_list:
            temp = []
            cur_s = 0
            for l in cur_list:
                if l.isInteger():
                    cur_s += l.getInteger()
                else:
                    temp += l.getList()
            mem[level] = cur_s
            level += 1
            cur_list = temp
        count = len(mem)
        res = 0
        for k, v in mem.items():
            res += (count - k) * v
        return res